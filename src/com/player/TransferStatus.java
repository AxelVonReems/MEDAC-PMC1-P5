package com.player;

/**
 * Transfer status options
 */
public enum TransferStatus {
    /**
     * Transfer status "Without request"
     */
    WITHOUT_REQUEST,
    /**
     * Transfer status "Requested"
     */
    REQUESTED,
    /**
     * Transfer status "Approved by coach"
     */
    APPROVED_BY_COACH,
    /**
     * Transfer status "Rejected by coach"
     */
    REJECTED_BY_COACH,
    /**
     * Transfer status "Approved by president"
     */
    APPROVED_BY_PRESIDENT,
    /**
     * Transfer status "Rejected by president"
     */
    REJECTED_BY_PRESIDENT
}
